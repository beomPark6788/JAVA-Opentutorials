
public class Variable {

	public static void main(String[] args) {
		
		int a = 1; // 데이터타입 변수명 값 Number -> integer 정수  ..-4,-3,-2,-1,0,1,2,3,4
		System.out.println(a); // 1
		
		double b = 1.1d; // 실수 real number -> double ... -2.0,-1.0,0,1.0,2.0 ...
		System.out.println(b); // 1.1
		
		String c = "Hello World"; // 문자열 c
		System.out.println(c); // Hello World
		
		// 어떤 데이터타입을 담을수있는지 명확하게 표현해야한다. 
		// 왜 정의해 줘야 할까 ? 생각 
	}
}
