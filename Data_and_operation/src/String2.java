
public class String2 {

	public static void main(java.lang.String[] args) {
		
		System.out.println("Hello World"); // String 문자가 모여있는 것
		System.out.println('H'); // Character 캐릭터는 한글자를 표현하는 데이터타입
		System.out.println("H"); // String 
		
		System.out.println("Hello "
				+ "World");
		// new line 새로운 줄바꿈
		System.out.println("Hello \n"
				+ "World");
		// escape
		System.out.println("Hello \"World\"");// Hello "World"  \ 쓰면 일시적으로 해방시켜준다 이스케이프 
		
	}

}
