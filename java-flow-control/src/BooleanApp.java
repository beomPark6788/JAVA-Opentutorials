
public class BooleanApp {
	public static void main(String[] args) {
		
		System.out.println("One"); // 문자열 
		System.out.println(1); // 정수 
		System.out.println(true); // boolean
		System.out.println(false);
		
		String foo = "Hello world";
//		String true = "Hello world"; // reserved word 예약어 변수명은 예약어 안된다.
		
		System.out.println(foo.contains("Hello")); // 글자가 포함되어 있나 ? true 반환 
		System.out.println(foo.contains("love")); // false 
		
		// true false 로 구체적인 데이터 타입이 boolean 데이터 타입이다. 
		
		
		System.out.println("1"+"1"); // 11  문자열을 만들어내는 연산자
		
		
	}
}
